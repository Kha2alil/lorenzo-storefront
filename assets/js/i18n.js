let currentLang = localStorage.getItem('lorenzo_lang') || 'en';

function setLanguage(lang) {
  if (!LANG[lang]) return;
  currentLang = lang;
  localStorage.setItem('lorenzo_lang', lang);
  // Set direction
  document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
  document.documentElement.lang = lang;
  // Update all data-i18n elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    const keys = key.split('+');
    if (keys.length > 1) {
      el.innerHTML = keys.map(k => t(k)).join('');
    } else {
      const val = t(key);
      if (val !== undefined) {
        if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
          el.placeholder = val;
        } else if (el.tagName === 'LABEL' && el.hasAttribute('for')) {
          el.textContent = val;
        } else {
          el.innerHTML = val;
        }
      }
    }
  });
  // Update page <title>
  const pageTitle = t('page.title');
  if (pageTitle && pageTitle !== 'page.title') document.title = pageTitle;
  // Update title attributes
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    const val = t(el.dataset.i18nTitle);
    if (val !== undefined) el.title = val;
  });
  // Update aria-label attributes
  document.querySelectorAll('[data-i18n-aria]').forEach(el => {
    const val = t(el.dataset.i18nAria);
    if (val !== undefined) el.setAttribute('aria-label', val);
  });
  // Update active switcher button
  document.querySelectorAll('.lang-switcher-btn').forEach(b => {
    b.classList.toggle('active', b.dataset.lang === lang);
  });
  // Dispatch event for JS modules
  document.dispatchEvent(new CustomEvent('langchange', { detail: lang }));
}

function t(key) {
  return LANG[currentLang] && LANG[currentLang][key] !== undefined ? LANG[currentLang][key] :
         LANG.en[key] !== undefined ? LANG.en[key] : key;
}

function setElementText(elOrId, key) {
  const el = typeof elOrId === 'string' ? document.getElementById(elOrId) : elOrId;
  if (el) el.innerHTML = t(key);
}
